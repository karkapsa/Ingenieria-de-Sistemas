public class Sumar {
    
    double resultado, resultado1; 
    
    public double CalcularResultado(double valor1, double valor2)
    {
         resultado = valor1 + valor2;
         return resultado;
         
    }
        
     public double calcularresta (double valor1, double valor2)
     {
                 
         resultado1 = valor1 - valor2;
         return resultado1;
    }
            
}

